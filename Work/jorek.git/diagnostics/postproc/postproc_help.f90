        write(*,*) ''
        write(*,*) 'Examples:'
        write(*,*) '  energy3d'
     case ( 'zeroD_quantities' ) 
        write(*,*) 'Usage:'
        write(*,*) '  zeroD_quantities'
        write(*,*) ''
        write(*,*) 'Prints a file with many 0D quantities over time'
        write(*,*) 'A list with column numbers and corresponding quantities'
