SUBROUTINE CUBFCT(S,SL,SU,H)
!-----------------------------------------------------------------------
!
!-----------------------------------------------------------------------
implicit none
REAL*8  s, sl, su, ds, q1, q2, H(4)
  DS= SU-SL
  Q1= (S-SL)/DS
  Q2= (SU-S)/DS
  H(1)= 3.d0*Q1**2 - 2.d0*Q1**3
  H(2)= 3.d0*Q2**2 - 2.d0*Q2**3
  H(3)= (S-SU)*Q1**2
  H(4)= (S-SL)*Q2**2
RETURN
END

SUBROUTINE DCUBF(S,SL,SU,H)
!-----------------------------------------------------------------------
!
!-----------------------------------------------------------------------
implicit none
real*8 s, sl, su, H(4), ds
  DS= SU-SL
  H(1)=  6.d0*(S-SL)/DS**2 - 6.d0*(S-SL)**2/DS**3
  H(2)= -6.d0*(SU-S)/DS**2 + 6.d0*(SU-S)**2/DS**3
  H(3)= ((S-SU)*2.d0*(S-SL)+(S-SL)**2)/DS**2
  H(4)= ((S-SL)*2.d0*(S-SU)+(S-SU)**2)/DS**2
RETURN
END